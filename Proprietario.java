//INDENTAZIONE DIO SANTO
public class Proprietario {
	private String nome;
	//ASSOLUTAMENTE MAI ACCENTI IN JAVA
	private String citta;

	public Proprietario(String nome, String città) {
		this.nome = nome;
		this.citta = città;
	}

	public String getNome() {
		return nome;
	}

	public String getCitta() {
		return citta;
	}
}
